
public class ifElse01P95 {
	public static void main(String[] args) {
		// if~else 구문은 참 일때 실행할 구문과 거짓일 때 실행할 구문을 작성한다.
		// if~else 구문은 한 구문으로 간주되기 때문에 연산횟수를 절약한다.
		// else문은 if문 없이 단독으로 쓰일 수 없다.
		// if문에만 조건식을 작성해야 하고 else 문은 조건식을 작성할 필요가 없다.
		int a = 15;
		if(a == 15) {
			System.out.println("a는 15이다.");
		} else {
			System.out.println("a는 15가 아니다.");
		}
	}
}
