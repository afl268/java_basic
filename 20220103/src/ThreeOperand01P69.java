
public class ThreeOperand01P69 {

	public static void main(String[] args) {
		// 삼항연산자는 (조건식 > 참일 때 결과 : 거짓일 때 결과) 형식으로 작성한다.
		// 조건식의 결과가 true라면 참일 때 결과에 적은 값이 출력되고
		// 조건식의 결과가 false라면 거짓 일 때 결과로 적은 값이 출력된다.
		System.out.println((3 < 5 ? "참입니다." : "거짓입니다."));
		System.out.println((6 < 5 ? "참입니다." : "거짓입니다."));
	}
}
